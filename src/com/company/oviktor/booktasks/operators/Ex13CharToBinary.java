package com.company.oviktor.booktasks.operators;

public class Ex13CharToBinary {

    public static void main(String[] args) {

        char a = 'A';
        char b = 'a';
        char c = '!';
        char d = ',';

        System.out.println("a : " + Integer.toBinaryString(a));
        System.out.println("a : " + Integer.toBinaryString(b));
        System.out.println("a : " + Integer.toBinaryString(c));
        System.out.println("a : " + Integer.toBinaryString(d));
    }
}
