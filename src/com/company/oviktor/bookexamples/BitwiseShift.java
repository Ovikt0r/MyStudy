package com.company.oviktor.bookexamples;

public class BitwiseShift {

    public static void main(String[] args) {

        int a = 8;
        System.out.println(Integer.toBinaryString(a));
        System.out.println(Integer.toBinaryString(a<<=1));
        System.out.println(a);
        System.out.println("a");
        int b = 8;
        System.out.println(Integer.toBinaryString(b));
        System.out.println(Integer.toBinaryString(b>>=1));
        System.out.println(b);
        System.out.println("b");
        int c = 8;
        System.out.println(Integer.toBinaryString(c));
        System.out.println(Integer.toBinaryString(c>>>=1));
        System.out.println(c);
        System.out.println("c");
        int d = 8;
        System.out.println(Integer.toBinaryString(d));
        System.out.println(Integer.toBinaryString(d<<=3));
        System.out.println(d);
        System.out.println("d");
        int e = 64;
        System.out.println(Integer.toBinaryString(e));
        System.out.println(Integer.toBinaryString(e>>=3));
        System.out.println(e);
        System.out.println("e");
        int g = 8;
        System.out.println(Integer.toBinaryString(g));
        System.out.println(Integer.toBinaryString(g>>>=2));
        System.out.println(g);
        System.out.println("g");
        int a1 = -80;
        System.out.println(Integer.toBinaryString(a1));
        System.out.println(Integer.toBinaryString(a1<<=1));
        System.out.println(a1);
        System.out.println("a1");
        int b1 = -8;
        System.out.println(Integer.toBinaryString(b1));
        System.out.println(Integer.toBinaryString(b1>>=1));
        System.out.println(b1);
        System.out.println("b1");
        int c1 = -8;
        System.out.println(Integer.toBinaryString(c1));
        System.out.println(Integer.toBinaryString(c1>>>=1));
        System.out.println(c1);
        System.out.println("c1");
        int d1 = -8;
        System.out.println(Integer.toBinaryString(d1));
        System.out.println(Integer.toBinaryString(d1<<=2));
        System.out.println(d1);
        System.out.println("d1");
        int e1 = -8;
        System.out.println(Integer.toBinaryString(e1));
        System.out.println(Integer.toBinaryString(e1>>=2));
        System.out.println(e1);
        System.out.println("e1");
        int g1 = -8;
        System.out.println(Integer.toBinaryString(g1));
        System.out.println(Integer.toBinaryString(g1>>>=2));
        System.out.println(g1);
        System.out.println("g1");
    }
}
